# Rule of Creating Variables in python
''' MultiLine Comment with the help of single triples
1. A-Z      : Uppercase Alphabets
2. a-z      : Lowercase Alphabets
3. _        : Special Character i.e. Underscore _
4. 0-9      : Digits
5. A-Za-z   : Combination of Uppercase and Lowercase Alphabets i.e. A-Za-z
6. A-Za-z_   : Combination of Uppercase,Lowercase Alphabets and special character i.e. _
7. A-Za-z_0-9   : Combination of Uppercase,Lowercase Alphabets, special character i.e. _ and digits i.e. 0-9
Note: A variable can not be created with prefix as a digit i.e. 0-9
'''
print("Uppercase Alphabets i.e. A-Z")
print("")
print("Lowercase Alphabets i.e. a-z")
print("")
print("Special Character i.e. Underscore _")
print("")
print("Digits i.e. 0-9")
print("")
print("Combination of Uppercase and Lowercase Alphabets i.e. A-Za-z")
print("")
print("Combination of Uppercase,Lowercase Alphabets and special character i.e. _")
print("")
print("Combination of Uppercase,Lowercase Alphabets, special character i.e. _ and digits i.e. 0-9")
print("")
print("Note: A variable can not be created with prefix as a digit i.e. 0-9")